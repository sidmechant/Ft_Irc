#include "Client.hpp"

/********************************************************************************/
/* ------------------------------- CONSTRUCTOR -------------------------------- */
/********************************************************************************/

Client::Client(void) {
    cmdRegister[0] = false;
    cmdRegister[1] = false;
    cmdRegister[2] = false;
    cmdRegister[3] = false;
    return;
}

/********************************************************************************/
/* -------------------------------- DESTRUCTOR -------------------------------- */
/********************************************************************************/

Client::~Client(void) { return; }

/********************************************************************************/
/* --------------------------------- METHODS ---------------------------------- */
/********************************************************************************/

bool	Client::isRegister(void) {
	if (cmdRegister[3] != true)
		if (cmdRegister[0] == true && cmdRegister[1] == true && cmdRegister[2] == true)
			return true;
	return false;
}

